# str_idx.py - String indexing and str() conversion

s: str = "hello"
print(s[0])
print(s[4])
print(s[1])
print(len(s))

# str() conversion
print(str(42))
print(str(-7))
print(str(0))

# string concatenation with str()
msg: str = "value=" + str(100)
print(msg)

# single char string
c: str = "A"
print(c[0])
print(len(c))

print("done")
